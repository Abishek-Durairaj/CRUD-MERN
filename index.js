const express = require("express")
const mongoose = require("mongoose")
const userModal = require("./userModal")
const cors = require("cors")
const app = express()
app.use(express.json())
app.use(cors())

mongoose.connect("mongodb://127.0.0.1:27017/crud").then(()=>{console.log("db connected")})
.catch((err)=>{
    console.log(err)
})


app.get('/',(req,res,next)=>{
    userModal.find({}).then(users=> res.json(users))
    .catch(err=> res.json(err))
})

app.post('/create',(req,res)=>{
userModal.create(req.body).then(users=> res.json(users))
.catch(err=> res.json(err))
})

app.get('/getUser/:id',(req,res)=>{
    const id = req.params.id
    userModal.findById({_id : id }).then(users=> res.json(users))
    .catch(err=> res.json(err))
    })

app.put('/update/:id',(req,res)=>{
    const id = req.params.id
    userModal.findByIdAndUpdate({_id :id},{name :req.body.name,email:req.body.email,
    age:req.body.age}).then(users=> res.json(users))
    .catch(err=> res.json(err))
    })

    app.delete('/deleteUser/:id',(req,res)=>{
        const id = req.params.id
        userModal.findByIdAndDelete({_id : id }).then(res=> res.json(res))
        .catch(err=> res.json(err))
        })


